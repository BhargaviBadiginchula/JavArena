public class ShiftDemo {
    public static void main(String[] args) {
        int a = 8; // 1000
        System.out.println("value of a is: "+a);
        System.out.println("a << 1 = " + (a << 1)); // 16
        System.out.println("a >> 1 = " + (a >> 1)); // 4
        System.out.println("a >>> 1 = " + (a >>> 1)); // 4
    }
}